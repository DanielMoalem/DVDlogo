let shapeColorChanged = false;

function setup() {
  createCanvas(712, 400);

  shapeColor = color(255, 0, 0); // Initial shape color
}

function draw() {
  background(0,10); // Black background
  // Check if the cursor is at the canvas edges
  if (mouseX <= 0 || mouseY <= 0 || mouseX >= width || mouseY >= height) {
    // Change the shape's color if it hasn't been changed already
    if (!shapeColorChanged) {
      shapeColor = color(random(255), random(255), random(255));
      shapeColorChanged = true; // Set the flag to true to indicate color change
    }
  } else {
    // Reset the color change flag
    shapeColorChanged = false;
  }

  // Set the fill color to the current shape color
  fill(shapeColor);

  // Draw the shape at the cursor's position
  beginShape();
  for (let i = 0; i < shapeVertices.length; i++) {
    vertex(
      shapeVertices[i].x + mouseX - 200,
      shapeVertices[i].y + mouseY - 200
    );
  }
  endShape(CLOSE);
}
